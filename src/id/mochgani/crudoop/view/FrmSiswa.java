    private void btnambilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnambilActionPerformed
        int index=tbl.getSelectedRow();
        if(index!=-1){
            Siswa s=tabelsisw.getSiswa(tbl.convertRowIndexToModel(index));
            txtnis.setText(s.getNis());
            txtnama.setText(s.getNama());
            lbljenis.setText(s.getJenis());
            if (lbljenis.getText().equals("L")) {
                radiolaki.setSelected(true);
                radioperempuan.setSelected(false);
            }else{
                radioperempuan.setSelected(true);
                radiolaki.setSelected(false);
            }
            txtkelas.setText(s.getKelas());
            txtalamat.setText(s.getAlamat());
            
            getCondition();
        }else{
            JOptionPane.showMessageDialog(null, "seleksi salah satu baris!");
        }
    }//GEN-LAST:event_btnambilActionPerformed

