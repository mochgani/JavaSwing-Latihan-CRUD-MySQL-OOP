    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        TampilWorker tampilWorker = new TampilWorker();
        if (validasiInput()==true) {
            String nis = txtnis.getText();
            String nama = txtnama.getText();
            String jenis = lbljenis.getText();
            String kelas = txtkelas.getText();
            String alamat = txtalamat.getText();
            
            Siswa s = new Siswa();
            s.setNis(nis);
            s.setNama(nama);
            s.setJenis(jenis);
            s.setKelas(kelas);
            s.setAlamat(alamat);
            
            sisw.insertSiswa(s);
            tabelsisw.insertSiswa(s);
            tampilWorker.execute();
            loadData();
            resetForm();
            newCondition();
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        TampilWorker tampilWorker = new TampilWorker();
        int index = tbl.getSelectedRow();
        if (index!=-1) {
            Siswa s = tabelsisw.getSiswa(tbl.convertRowIndexToModel(index));
            if (JOptionPane.showConfirmDialog(null, "Apakah Yakin Di Hapus?","konfirmasi", JOptionPane.OK_CANCEL_OPTION)== JOptionPane.OK_OPTION) {
                sisw.deleteSiswa(s);
                tampilWorker.execute();
                tabelsisw.deleteSiswa(index);
                loadData();
                newCondition();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleksi Salah satu baris!!");
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        TampilWorker tw = new TampilWorker();
        int index = tbl.getSelectedRow();
        if (index!=-1) {
            Siswa s = tabelsisw.getSiswa(tbl.convertRowIndexToModel(index));
            if (validasiInput()==true) {
                String nis = txtnis.getText();
                String nama = txtnama.getText();
                String jenis = lbljenis.getText();
                String kelas = txtkelas.getText();
                String alamat = txtalamat.getText();

                s.setNama(nama);
                s.setJenis(jenis);
                s.setKelas(kelas);
                s.setAlamat(alamat);
                s.setNis(nis);

                sisw.updateSiswa(s);
                tw.execute();
                tabelsisw.updateSiswa(index, s);
                resetForm();
                loadData();
                
                newCondition();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Tekan Button Ambil Data Dahulu");
        }
    }//GEN-LAST:event_btnubahActionPerformed

